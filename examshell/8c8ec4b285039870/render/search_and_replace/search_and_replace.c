#include <unistd.h>

int main(int argc, char *argv[])
{
    if(argc!=4)
    {
        write(1, "\n", 1);
    }

    int i;
    i=0;
    while(argv[1][i]!='\0' && argv[2][i]!='\0' && argv[3][i]!='\0')
    {
        if(argv[1][i]!=argv[2][i])
        {
            write(1, "2", 1);
        }
    }
    write(1, "n", 1);
}