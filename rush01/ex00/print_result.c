#include <unistd.h>

void print_result(char **arr_result)
{
    int i;
    int j;

    i = 0;
    while (i < 4)
    {
        j = 0;
        while (j < 4)
        {
            write(1, &arr_result[i][j], 1);
            j++;
        }
        write(1, "\n", 1);
        i++;
    }
}